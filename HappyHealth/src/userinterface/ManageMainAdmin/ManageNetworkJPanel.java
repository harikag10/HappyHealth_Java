    private void btnAddCityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCityActionPerformed
        // TODO add your handling code here:
         String name = jCountryComboBox.getSelectedItem().toString();
        Network network;
        Boolean check = true;
        //System.out.println(name);
        if(name.equalsIgnoreCase("Select")){
          JOptionPane.showMessageDialog(null, "Please select a Country");
        }
        
        else{
           if(system.getNetworkList().isEmpty()){
               
              network = system.createAndAddNetwork();
                network.setName(name);

        populateNetworkTable();
       
           }
           
           else{
            for (Network net : system.getNetworkList()) {
            
            if(name.equalsIgnoreCase(net.getName())){
            
            JOptionPane.showMessageDialog(null, "Country already exists!!");
            check = false;
             break;
        }
       
       
    }                                             
           
        if(check){
            
         network = system.createAndAddNetwork();
        network.setName(name);

        populateNetworkTable();
        //break;
            
        }}
           }
        
    }//GEN-LAST:event_btnAddCityActionPerformed

    private void btnDeleteCityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCityActionPerformed
        // TODO add your handling code here:
        int row = tableNetwork.getSelectedRow();
        if(row>=0){
        Network network = (Network) tableNetwork.getValueAt(row,0);
        system.getNetworkList().remove(network);
        populateNetworkTable();
        }
        else if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row from table","Warning",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnDeleteCityActionPerformed


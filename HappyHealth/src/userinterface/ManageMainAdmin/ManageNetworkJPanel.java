    private void populateNetworkTable() {
        //System.out.println("IN populate");
        DefaultTableModel model = (DefaultTableModel) tableNetwork.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            Object[] row = new Object[1];
            row[0] = network;
            model.addRow(row);
        }
    }

